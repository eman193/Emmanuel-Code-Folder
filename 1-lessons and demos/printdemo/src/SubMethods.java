public class SubMethods {
    private int ans = 0;

    public void calc(int a, int b) {
        ans = (a * b);
    }
    public void output() {
        System.out.println(ans);
        System.out.printf("%.2f",ans);

    
        /*
         * %f - real/decimal num
         * %d - int value
         * %c - char value
         * %s - strin value
         */

    }
}
